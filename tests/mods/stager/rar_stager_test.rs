#[test]
fn test_stage_with_flat_rar_returns_entry_named_after_stripped_name() {
    let temp = tempdir().unwrap();
    let rar_path = temp.path().join("SomeMod.rar");
    std::fs::write(&rar_path, RAR_WITH_ONE_LOOSE_FILE).unwrap();

    let entry = ModEntry {
        name: "SomeMod.rar".into(),
        path: rar_path,
        kind: ModEntryKind::RarArchive,
        metadata: None,
    };
    let staging_path = temp.path().join("staging");

    let result = RarStager::stage(&entry, &staging_path).unwrap();

    assert_eq!(result.name, "SomeMod");
    assert_eq!(result.path, staging_path.join("SomeMod"));
    assert_eq!(result.kind, ModEntryKind::Directory);
    assert!(result.metadata.is_none());
    assert!(staging_path.join("SomeMod").join("VERSION").exists());
}

#[test]
fn test_stage_wrap_dir_rar_returns_entry_named_after_stripped_name() {
    let temp = tempdir().unwrap();
    let rar_path = temp.path().join("SomeMod.rar");
    std::fs::write(
        &rar_path,
        RAR_WITH_STANDARD_WRAP_DIR_MULTIPLE_FILES_AND_RECURSIVE_FOLDERS,
    )
    .unwrap();

    let entry = ModEntry {
        name: "SomeMod.rar".into(),
        path: rar_path,
        kind: ModEntryKind::RarArchive,
        metadata: None,
    };
    let staging_path = temp.path().join("staging");

    let result = RarStager::stage(&entry, &staging_path).unwrap();

    assert_eq!(result.name, "WrapDir");
    assert_eq!(result.path, staging_path.join("WrapDir"));
    assert_eq!(result.kind, ModEntryKind::Directory);
    assert!(result.metadata.is_none());
    assert!(staging_path
        .join("WrapDir")
        .join("folder")
        .join("file.txt")
        .exists());
    assert!(staging_path.join("WrapDir").join("file.txt").exists());
    assert_eq!(
        std::fs::read_to_string(staging_path.join("WrapDir").join("file.txt")).unwrap(),
        "file content"
    );
    assert_eq!(
        std::fs::read_to_string(staging_path.join("WrapDir").join("folder").join("file.txt"))
            .unwrap(),
        "folder content"
    );
}

#[test]
fn test_unstage_removes_directory() {
    let temp = tempdir().unwrap();
    let dir = temp.path().join("my_mod");
    std::fs::create_dir(&dir).unwrap();
    std::fs::write(dir.join("file.txt"), "data").unwrap();

    RarStager::unstage(&dir).unwrap();

    assert!(!dir.exists());
}

#[test]
fn test_unstage_not_found_does_not_error() {
    let temp = tempdir().unwrap();
    let dir = temp.path().join("nonexistent_mod");

    let result = RarStager::unstage(&dir);

    assert!(result.is_ok());
}

